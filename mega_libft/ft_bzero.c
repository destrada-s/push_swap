#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		*((char *)s + i) = 0;
		i++;
	}
}
/*to give values to a void *(pointer) you have to cast the type
	of the input you are going to input to the void pointer*/
